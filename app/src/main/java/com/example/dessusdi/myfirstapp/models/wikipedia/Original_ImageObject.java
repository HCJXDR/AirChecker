public class Original_ImageObject {
    private final String source;
    private final int width;
    private final int height;

    public Original_ImageObject() {
        this.source = "";
        this.width = 0;
        this.height = 0;
    }

    public String getSource() {
        return source;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
